import {
  NavigationContainer,
  DefaultTheme,
  DarkTheme,
} from "@react-navigation/native";
import { createStackNavigator } from "@react-navigation/stack";
import * as React from "react";
import { ColorSchemeName } from "react-native";
import LoginScreen from "../screens/Auth/LoginScreen";

import NotFoundScreen from "../screens/NotFoundScreen";
import { ScreenStackParamList } from "../../types";
import LinkingConfiguration from "./LinkingConfiguration";
import HomePage from "../screens/HomePage/HomePage";
import SelectedBook from "../screens/Book/SelectedBook";

// This is it the entire application navigation wrapped in a navigation container
export default function Navigation() {
  return (
    <NavigationContainer linking={LinkingConfiguration}>
      <RootNavigator />
    </NavigationContainer>
  );
}

// Stack to hold the pages of the app
const Stack = createStackNavigator<ScreenStackParamList>();

function RootNavigator() {
  return (
    <Stack.Navigator screenOptions={{ headerShown: false }}>
      <Stack.Screen name="Login" component={LoginScreen} />
      <Stack.Screen name="HomePage" component={HomePage} />
      <Stack.Screen name="SelectedBook" component={SelectedBook} />
      <Stack.Screen
        name="NotFound"
        component={NotFoundScreen}
        options={{ title: "Oops!" }}
      />
    </Stack.Navigator>
  );
}

